-- -------------------------------------------------------
-- Convenience views
-- -------------------------------------------------------

-- Quick overview: tables recommended for replication
CREATE VIEW replication_planner.v_replicate_targets AS
SELECT
    schemaname,
    tablename,
    round(final_score::numeric, 4)      AS score,
    decision,
    hot_index_cols,
    row_filter_hint
FROM replication_planner.plan_results
WHERE decision IN ('REPLICATE_ALL', 'REPLICATE_FILTERED')
ORDER BY final_score DESC;

COMMENT ON VIEW replication_planner.v_replicate_targets IS
    'Tables the planner recommends replicating, ordered by score.';

-- Index heat leaderboard
CREATE VIEW replication_planner.v_index_heat AS
SELECT
    schemaname,
    tablename,
    indexname,
    idx_scans,
    round(hit_ratio::numeric, 4)        AS hit_ratio,
    round(scan_share::numeric, 4)       AS scan_share,
    round(heat_score::numeric, 4)       AS heat_score,
    recommendation,
    index_columns
FROM replication_planner.collect_index_stats()
ORDER BY heat_score DESC;

COMMENT ON VIEW replication_planner.v_index_heat IS
    'Index heat leaderboard. Use to identify dominant read indexes.';

-- Workload top-10
CREATE VIEW replication_planner.v_hot_queries AS
SELECT
    heat_tier,
    calls,
    round(call_share::numeric * 100, 2) AS call_pct,
    round(mean_exec_ms::numeric, 2)     AS mean_ms,
    contains_where,
    query_snippet
FROM replication_planner.collect_workload_stats()
WHERE heat_tier IN ('CRITICAL', 'HOT')
ORDER BY calls DESC
LIMIT 50;

COMMENT ON VIEW replication_planner.v_hot_queries IS
    'Top-50 hottest SELECT queries from pg_stat_statements.';

-- Histogram distribution for a given table/column
-- (pure SQL, uses pg_stats)
CREATE VIEW replication_planner.v_column_histograms AS
SELECT
    schemaname,
    tablename,
    attname                             AS column_name,
    n_distinct,
    correlation,
    most_common_vals::text              AS top_values,
    most_common_freqs::text             AS top_freqs,
    histogram_bounds::text              AS histogram_bounds,
    null_frac
FROM pg_stats
WHERE schemaname NOT IN ('pg_catalog','information_schema')
ORDER BY schemaname, tablename, attname;

COMMENT ON VIEW replication_planner.v_column_histograms IS
    'Exposes pg_stats histogram and MCV data for all user table columns.';

-- -------------------------------------------------------
-- GUC documentation view
-- -------------------------------------------------------
CREATE VIEW replication_planner.v_settings AS
SELECT name, setting, unit, short_desc
FROM pg_settings
WHERE name LIKE 'replication_planner.%'
ORDER BY name;

COMMENT ON VIEW replication_planner.v_settings IS
    'Shows current replication_planner GUC parameter values.';
